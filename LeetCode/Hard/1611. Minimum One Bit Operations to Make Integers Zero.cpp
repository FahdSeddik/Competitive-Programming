class Solution {
public:
    int minimumOneBitOperations(int n) {
        int ans=0,f=1;
        for(int i=31;i>=0;i--)if((n>>i)&1)ans+=f*((1<<(i+1))-1),f*=-1;
        return ans;
    }
};
